def colormap(img, cmap="jet"):
    import matplotlib.pyplot as plt

    W, H = img.shape[:2]
    dpi = 300
    fig, ax = plt.subplots(1, figsize=(H / dpi, W / dpi), dpi=dpi)
    im = ax.imshow(img, cmap=cmap)
    ax.set_axis_off()
    fig.colorbar(im, ax=ax)
    fig.tight_layout()
    fig.canvas.draw()
    data = np.frombuffer(fig.canvas.tostring_rgb(), dtype=np.uint8)
    data = data.reshape(fig.canvas.get_width_height()[::-1] + (3,))
    img = torch.from_numpy(data / 255.0).float().permute(2, 0, 1)
    plt.close()
    return img


def write_json(
    train_cam_infos,
    path,
):
    frames = []
    save_path = os.path.join(path, "train_cameras.json")
    for train_cam in train_cam_infos:
        image_path = "results/" + train_cam.image_path.split("/")[-1]
        depth_path = image_path.replace("frame", "depth").replace("jpg", "png")
        Rt = np.zeros((4, 4))
        Rt[:3, :3] = train_cam.R.transpose()
        Rt[:3, 3] = train_cam.T
        Rt[3, 3] = 1.0

        c2w = np.linalg.inv(Rt)
        frame = {
            "file_path": image_path,
            "transform_matrix": c2w.tolist(),
            "depth_file_path": depth_path,
        }
        frames.append(frame)
    out = {}
    train_example = train_cam_infos[0]
    out["fl_x"] = fov2focal(train_example.FovX, train_example.width)
    out["fl_y"] = fov2focal(train_example.FovY, train_example.height)
    out["k1"] = 0
    out["k2"] = 0
    out["p1"] = 0
    out["p2"] = 0
    out["cx"] = int(train_example.width / 2)
    out["cy"] = int(train_example.height / 2)
    out["w"] = train_example.width
    out["h"] = train_example.height
    out["frames"] = frames
    with open(save_path, "w", encoding="utf-8") as f:
        json.dump(out, f, indent=4)


def load_from_json(filename: Path):
    assert filename.suffix == ".json"
    with open(filename, encoding="UTF-8") as file:
        return json.load(file)
